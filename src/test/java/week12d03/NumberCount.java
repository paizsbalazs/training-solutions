package week12d03;

public class NumberCount {

  private int szam;
  private int darab;

    public NumberCount(int szam, int darab) {
        this.szam = szam;
        this.darab = darab;
    }

    public int getDarab() {
        return darab;
    }

    public int getSzam() {
        return szam;
    }
}
